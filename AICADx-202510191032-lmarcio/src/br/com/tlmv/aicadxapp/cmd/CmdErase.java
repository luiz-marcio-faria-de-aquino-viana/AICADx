public class CmdErase extends CmdBase
{
//Public

	public CmdErase() {
		super(AppDefs.ACTION_EDIT2_ERASE, false, true);
	}
	
	/* Methodes */
	
	@Override
	public InputParamVO promptInputParam(MainFrame frm, InputParamVO oParam)
	{
		InputParamVO result = null;
		
		PromptUtil.prompt("Erase object...");
		
		CadEntity oEnt = PromptUtil.selectObject("Select object: ");
		if(oEnt == null) return null;;
		
		result = new InputParamVO();
		result.initEntity(oEnt);
		
		return result;
	}
	
	@Override
	public void doCommand() {
		InputParamVO oParam = this.promptInputParam(this.getFrm(), null);
		if(oParam == null) return;
		
		//CADPOINT
		//
		CadEntity oEnt = oParam.getEnt1();
		if(oEnt != null) {
			oEnt.setDeleted(true);
		}
	}

}
