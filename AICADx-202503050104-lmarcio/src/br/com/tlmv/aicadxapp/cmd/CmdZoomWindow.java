public class CmdZoomWindow implements CmdBase, Runnable
{
//Private
	private AppMain app = null;
	private MainFrame frm = null;
	private AppCadMain cad = null;
	private CadDocumentDef doc = null;

	private Thread thread = null;
	private boolean bRunning = false;
	
	/* Methodes */
	
	private InputParamVO promptInputParam(MainFrame frm)
	{
		InputParamVO result = null;
		
		PromptUtil.prompt("Zoom window...");

    	GeomPoint2d ptFirstCornerMcs = PromptUtil.zoomFirstCorner2d(null, "First corner: ");
		if(ptFirstCornerMcs == null) return null;
		
		GeomPoint2d ptSecondCornerMcs = PromptUtil.zoomSecondCorner2d(ptFirstCornerMcs, "Second corner: ");
		if(ptSecondCornerMcs == null) return null;

		GeomPoint3d ptMin3d = GeomPoint3d.lowerLeftCornerFrom(ptFirstCornerMcs, ptSecondCornerMcs);
		GeomPoint3d ptMax3d = GeomPoint3d.upperRightCornerFrom(ptFirstCornerMcs, ptSecondCornerMcs);
		
		result = new InputParamVO();
		result.initRectangle(ptMin3d, ptMax3d);

		return result;
	}
	
//Public
	
	@Override
	public int executeCommand(AppMain app, MainFrame frm, AppCadMain cad, CadDocumentDef doc) 
	{
		this.app = app;
		this.frm = frm;
		this.cad = cad;
		this.doc = doc;
		
		this.thread = new Thread(this);
		this.thread.start();
		
		return AppDefs.RSOK;
	}

	/* THREADS */
	
	@Override
	public void run() {
		this.bRunning = true;
		
		InputParamVO oParam = this.promptInputParam(frm);
		if(oParam == null) return;
		
		//GEOMPOINT3D
		//
		GeomPoint2d ptMin2d = new GeomPoint2d(oParam.getPtMin());
		GeomPoint2d ptMax2d = new GeomPoint2d(oParam.getPtMax());
		
		//ZOOM_WINDOW
		//
		MainPanel panel = MainPanel.getPanel();

		ICompView v = panel.getCurrView();
		
		ICadViewBase cv = v.getCadViewBase();
		cv.zoomWindowMcs(ptMin2d, ptMax2d);
		
		v.repaintAll();
		
		this.bRunning = false;		
	}
	
	/* Getters/Setters */
	
	public AppMain getApp() {
		return app;
	}

	public MainFrame getFrm() {
		return frm;
	}

	public AppCadMain getCad() {
		return cad;
	}

	public CadDocumentDef getDoc() {
		return doc;
	}

	public boolean isbRunning() {
		return bRunning;
	}

}
